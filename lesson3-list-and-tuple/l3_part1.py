# Creating a list
# ==================================================

fruits = ['apple', 'banana', 'apple', 'pear', 'strawberry']
print(fruits)

# Calling a list function
fruits2 = list(['apple', 'banana', 'apple', 'pear', 'strawberry'])
print(fruits2)

letters = list('apple')
print(letters) # ['a', 'p', 'p', 'l', 'e']

numbers = list(range(10))
print(numbers)

# Creating an empty list
empty_list = []
print(empty_list)

empty_list1 = list()
print(empty_list1)