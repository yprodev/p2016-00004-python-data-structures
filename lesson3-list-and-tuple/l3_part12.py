# Common methods - .Count And .Index
# ==================================================

fruits = ['apple', 'banana', 'apple', 'pear', 'strawberry']
print(fruits.count('apple')) # 2

profile = 'Ford', 'Madox', 'Ford'
print(profile.count('Ford')) # 2


print(fruits.index('pear')) # 3
print(profile.index('Madox')) # 1
